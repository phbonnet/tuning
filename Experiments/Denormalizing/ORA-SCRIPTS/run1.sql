L_QUANTITY, L_EXTENDEDPRICE, 
L_DISCOUNT,
L_TAX,
L_RETURNFLAG,
L_LINESTATUS,
L_SHIPDATE,
L_COMMITDATE,
L_RECEIPTDATE,
L_SHIPINSTRUCT,
L_SHIPMODE,
L_COMMENT,
R_NAME
from LINEITEM, REGION, SUPPLIER, NATION
